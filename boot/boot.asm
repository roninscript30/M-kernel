; ==========================================================
; M-Kernel Bootloader
; Day 1
; ==========================================================

BITS 16
ORG 0x7C00

start:

    ; Disable interrupts
    cli

    ; Halt CPU forever
.halt:
    hlt
    jmp .halt

; ----------------------------------------------------------
; Fill remaining bytes with zeros until byte 510
; ----------------------------------------------------------

times 510-($-$$) db 0

; ----------------------------------------------------------
; Boot Signature
; BIOS checks for 0x55AA
; ----------------------------------------------------------

dw 0xAA55
